package com.voda.voda_admin;

public class Menu {
    String name,text,text_addition,category;

    public Menu(){

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getText_addition() {
        return text_addition;
    }

    public void setText_addition(String text_addition) {
        this.text_addition = text_addition;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }
}
